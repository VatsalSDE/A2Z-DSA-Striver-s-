package basics;

public class shortcut_var {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);

        x += 7; // x = x + 7
        System.out.println(x);

        x -= 2; //x = x - 2
        System.out.println(x);

        x *= 6; //x = x * 6
        System.out.println(x);

        x /= 5; // x = x / 5
        System.out.println(x);

        x %= 2; // x = x % 2
        System.out.println(x);
    }
}
