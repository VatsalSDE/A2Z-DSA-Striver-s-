package basics;

public class updating_variables {
    public static void main(String[] args) {
        int x;
        x = 8;
        System.out.println(x);

        x = 10;
        System.out.println(x);

        x = x + 6;
        System.out.println(x);

        x = x - 20;
        System.out.println(x);
    }
}
